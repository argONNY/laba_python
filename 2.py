# Программа запрашивает у пользователя число n и выполняет вычисления с использованием цикла for.

# Ввод числа
n = int(input("Введите число n: "))

# Вывод всех чисел от 1 до n
print("Числа от 1 до n:")
for i in range(1, n + 1):
    print(i, end=" ")
print()

# Вывод квадратов чисел от 1 до n
print("Квадраты чисел от 1 до n:")
for i in range(1, n + 1):
    print(i ** 2, end=" ")
print()

# Вычисление суммы всех чисел от 1 до n
sum_numbers = sum(range(1, n + 1))
print(f"Сумма всех чисел от 1 до {n}: {sum_numbers}")
