# Программа запрашивает у пользователя три числа и определяет наибольшее, наименьшее и равны ли они.

# Ввод чисел
a = float(input("Введите первое число:"))
b = float(input("Введите второе число:"))
c = float(input("Введите третье число:"))

# Определение наибольшего и наименьшего
max_num = max(a, b, c)
min_num = min(a, b, c)

# Проверка на равенство всех чисел
are_equal = a == b == c

# Вывод результатов
print(f"Наибольшее число: {max_num}")
print(f"Наименьшее число: {min_num}")
print("Все числа равны" if are_equal else "Числа не равны")
